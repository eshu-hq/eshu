// TestLoadRuntimeConfigDoesNotRequireRedactionKeyForBedrock confirms a
// Bedrock-only target validates and that the Bedrock scanner needs no redaction
// key: it is metadata-only and is payload-blind by construction. It drops
// agent prompts, guardrail policies, knowledge base document content, and
// action-group schema bodies by never reading them, not by redacting them.
func TestLoadRuntimeConfigDoesNotRequireRedactionKeyForBedrock(t *testing.T) {
	getenv := mapEnv(map[string]string{
		"ESHU_COLLECTOR_INSTANCES_JSON": `[{
			"instance_id":"collector-aws-1",
			"collector_kind":"aws",
			"mode":"continuous",
			"enabled":true,
			"claims_enabled":true,
			"configuration":{
				"target_scopes":[{
					"account_id":"123456789012",
					"allowed_regions":["us-east-1"],
					"allowed_services":["bedrock"],
					"credentials":{
						"mode":"local_workload_identity"
					}
				}]
			}
		}]`,
		"ESHU_AWS_COLLECTOR_INSTANCE_ID": "collector-aws-1",
	})

	config, err := loadRuntimeConfig(getenv)
	if err != nil {
		t.Fatalf("loadRuntimeConfig() error = %v, want nil", err)
	}
	if got, want := config.AWS.Targets[0].AllowedServices[0], awscloud.ServiceBedrock; got != want {
		t.Fatalf("AllowedServices[0] = %q, want %q", got, want)
	}
	if !config.AWSRedactionKey.IsZero() {
		t.Fatalf("AWSRedactionKey configured for Bedrock-only target, want zero key")
	}
}
