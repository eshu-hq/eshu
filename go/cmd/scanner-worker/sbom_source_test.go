func countFactKinds(values []facts.Envelope) map[string]int {
	counts := make(map[string]int)
	for _, value := range values {
		counts[value.FactKind]++
	}
	return counts
}

func componentsByName(values []sbomgenerator.Component) map[string]sbomgenerator.Component {
	components := make(map[string]sbomgenerator.Component, len(values))
	for _, value := range values {
		components[value.Name] = value
	}
	return components
}

func assertComponentEvidence(
	t *testing.T,
	component sbomgenerator.Component,
	ecosystem string,
	lockfilePath string,
	dependencyScope string,
	dependencyType string,
	purl string,
) {
	t.Helper()

	if component.Name == "" {
		t.Fatalf("component for %s missing", purl)
	}
	if component.Ecosystem != ecosystem {
		t.Fatalf("%s ecosystem = %q, want %q", component.Name, component.Ecosystem, ecosystem)
	}
	if component.LockfilePath != lockfilePath {
		t.Fatalf("%s lockfile path = %q, want %q", component.Name, component.LockfilePath, lockfilePath)
	}
	if component.DependencyScope != dependencyScope {
		t.Fatalf("%s dependency scope = %q, want %q", component.Name, component.DependencyScope, dependencyScope)
	}
	if component.DependencyType != dependencyType {
		t.Fatalf("%s dependency type = %q, want %q", component.Name, component.DependencyType, dependencyType)
	}
	if component.PURL != purl {
		t.Fatalf("%s purl = %q, want %q", component.Name, component.PURL, purl)
	}
	if component.ExtractionReason != "lockfile_exact_version" {
		t.Fatalf("%s extraction reason = %q, want lockfile_exact_version", component.Name, component.ExtractionReason)
	}
}

func containsAny(value string, needles ...string) bool {
	for _, needle := range needles {
		if needle != "" && strings.Contains(value, needle) {
			return true
		}
	}
	return false
}
