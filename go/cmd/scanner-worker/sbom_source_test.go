func TestRepositorySBOMSourceParsesNPMPackageDependencyRanges(t *testing.T) {
	t.Parallel()

	root := t.TempDir()
	writeTestFile(t, filepath.Join(root, "package-lock.json"), `{
		"lockfileVersion": 3,
		"packages": {
			"": {
				"name": "test-app",
				"version": "1.0.0",
				"dependencies": {
					"@hapi/wreck": "^18.0.0",
					"range-only": "^1.0.0"
				}
			},
			"node_modules/@hapi/wreck": {
				"version": "18.0.0",
				"dependencies": {
					"@hapi/boom": "^10.0.1",
					"hoek": "9.x"
				}
			},
			"node_modules/@hapi/boom": {
				"version": "10.0.1"
			},
			"node_modules/hoek": {
				"version": "9.3.0",
				"optional": true
			}
		}
	}`)
	nestedDir := filepath.Join(root, "subdir")
	if err := os.MkdirAll(nestedDir, 0o700); err != nil {
		t.Fatalf("MkdirAll(%s) error = %v", nestedDir, err)
	}
	writeTestFile(t, filepath.Join(nestedDir, "package-lock.json"), `{
		"lockfileVersion": 3,
		"packages": {
			"": {
				"name": "nested-app",
				"version": "1.0.0",
				"dependencies": {
					"nested-exact": "^2.0.0",
					"nested-range-only": "^3.0.0"
				}
			},
			"node_modules/nested-exact": {
				"version": "2.0.1",
				"dependencies": {
					"nested-transitive": "~4.0.0"
				}
			},
			"node_modules/nested-transitive": {
				"version": "4.0.2"
			}
		}
	}`)
	source, err := newRepositorySBOMSource([]sbomTargetConfig{{
		ScopeID:  "scanner-worker://repository/repo-private-name",
		RootPath: root,
	}})
	if err != nil {
		t.Fatalf("newRepositorySBOMSource() error = %v, want nil", err)
	}

	inventory, err := source.Collect(context.Background(), testSBOMClaimInput(t))
	if err != nil {
		t.Fatalf("Collect() error = %v, want nil", err)
	}
	if len(inventory.Warnings) != 0 {
		t.Fatalf("Warnings len = %d, want 0 for npm package dependency ranges: %#v", len(inventory.Warnings), inventory.Warnings)
	}
	components := componentsByName(inventory.Components)
	assertComponentEvidence(t, components["@hapi/wreck"], "npm", "package-lock.json", "runtime", "direct", "pkg:npm/%40hapi/wreck@18.0.0")
	assertComponentEvidence(t, components["@hapi/boom"], "npm", "package-lock.json", "runtime", "direct", "pkg:npm/%40hapi/boom@10.0.1")
	assertComponentEvidence(t, components["hoek"], "npm", "package-lock.json", "optional", "direct", "pkg:npm/hoek@9.3.0")
	assertComponentEvidence(t, components["nested-exact"], "npm", "subdir/package-lock.json", "runtime", "direct", "pkg:npm/nested-exact@2.0.1")
	assertComponentEvidence(t, components["nested-transitive"], "npm", "subdir/package-lock.json", "runtime", "direct", "pkg:npm/nested-transitive@4.0.2")
	if _, ok := components["range-only"]; ok {
		t.Fatalf("range-only dependency emitted as installed component, want dependency range ignored without exact installed version")
	}
	if _, ok := components["nested-range-only"]; ok {
		t.Fatalf("nested-range-only dependency emitted as installed component, want dependency range ignored without exact installed version")
	}
}

func TestRepositorySBOMSourceEmitsMalformedLockfileWarning(t *testing.T) {
	t.Parallel()

	root := t.TempDir()
	writeTestFile(t, filepath.Join(root, "composer.lock"), `{"packages":[`)
	source, err := newRepositorySBOMSource([]sbomTargetConfig{{
		ScopeID:  "scanner-worker://repository/repo-private-name",
		RootPath: root,
	}})
	if err != nil {
		t.Fatalf("newRepositorySBOMSource() error = %v, want nil", err)
	}

	inventory, err := source.Collect(context.Background(), testSBOMClaimInput(t))
	if err != nil {
		t.Fatalf("Collect() error = %v, want nil; malformed lockfile should emit warning evidence", err)
	}
	if got := len(inventory.Components); got != 0 {
		t.Fatalf("Components len = %d, want 0 for malformed lockfile", got)
	}
	if got, want := len(inventory.Warnings), 1; got != want {
		t.Fatalf("Warnings len = %d, want %d", got, want)
	}
	warning := inventory.Warnings[0]
	if warning.Reason != "lockfile_malformed" {
		t.Fatalf("warning reason = %q, want lockfile_malformed", warning.Reason)
	}
	if warning.LockfilePath != "composer.lock" || warning.Ecosystem != "composer" {
		t.Fatalf("warning evidence = %#v, want composer.lock composer evidence", warning)
	}
	if containsAny(warning.Summary, root) {
		t.Fatalf("warning summary leaked private root path: %q", warning.Summary)
	}
}

func TestRepositorySBOMSourceEnforcesFileLimitWithoutLeakingPaths(t *testing.T) {
	t.Parallel()

	root := t.TempDir()
	writeTestFile(t, filepath.Join(root, "package-lock.json"), `{"lockfileVersion":3,"packages":{}}`)
	writeTestFile(t, filepath.Join(root, "go.mod"), "module example.test/app\n")

	source, err := newRepositorySBOMSource([]sbomTargetConfig{{
		ScopeID:  "scanner-worker://repository/repo-private-name",
		RootPath: root,
	}})
	if err != nil {
		t.Fatalf("newRepositorySBOMSource() error = %v, want nil", err)
	}
	input := testSBOMClaimInput(t)
	input.Limits.MaxFiles = 1

	_, err = source.Collect(context.Background(), input)
	if err == nil {
		t.Fatal("Collect() error = nil, want file limit failure")
	}
	var failure scannerworker.AnalyzerFailure
	if !errors.As(err, &failure) {
		t.Fatalf("Collect() error = %T, want scannerworker.AnalyzerFailure", err)
	}
	if got, want := failure.FailureClass(), scannerworker.FailureClassFileLimitExceeded; got != want {
		t.Fatalf("FailureClass = %q, want %q", got, want)
	}
	if got := err.Error(); containsAny(got, root, "package-lock.json", "go.mod") {
		t.Fatalf("Collect() error leaked private target detail: %q", got)
	}
}

