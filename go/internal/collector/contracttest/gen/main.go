// Command contracttest-gen reads specs/collector_fact_contract.v1.yaml and
// emits go/internal/collector/contracttest/contract_data.go. Pass -check to
// verify the committed output is current without writing.
package main

import (
	"bytes"
	"flag"
	"fmt"
	"io"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

type options struct {
	repoRoot string
	specPath string
	outPath  string
	check    bool
}

type contractSpec struct {
	Version    string                   `yaml:"version"`
	Collectors map[string]collectorSpec `yaml:"collectors"`
}

type collectorSpec struct {
	CollectorKind string              `yaml:"collector_kind"`
	FactKinds     map[string]factSpec `yaml:"fact_kinds"`
}

type factSpec struct {
	RequiredPayloadKeys []string `yaml:"required_payload_keys"`
}

func main() {
	if err := run(os.Args[1:], os.Stdout, os.Stderr); err != nil {
		_, _ = fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func run(args []string, stdout io.Writer, stderr io.Writer) error {
	opts, err := parseOptions(args, stderr)
	if err != nil {
		return err
	}
	spec, err := loadSpec(opts.specPath)
	if err != nil {
		return err
	}
	code, err := generateGo(spec)
	if err != nil {
		return err
	}
	if opts.check {
		if err := verifyFile(opts.outPath, code); err != nil {
			return err
		}
		_, _ = fmt.Fprintln(stdout, "contracttest-gen: generated contract data is current")
		return nil
	}
	if err := writeFile(opts.outPath, code); err != nil {
		return err
	}
	_, _ = fmt.Fprintf(stdout, "contracttest-gen: wrote %s\n", opts.outPath)
	return nil
}

func parseOptions(args []string, stderr io.Writer) (options, error) {
	flags := flag.NewFlagSet("contracttest-gen", flag.ContinueOnError)
	flags.SetOutput(stderr)
	opts := options{}
	flags.StringVar(&opts.repoRoot, "repo-root", "..", "repository root")
	flags.StringVar(&opts.specPath, "spec", "", "contract spec YAML path")
	flags.StringVar(&opts.outPath, "out", "", "output Go file path")
	flags.BoolVar(&opts.check, "check", false, "verify generated file without writing")
	if err := flags.Parse(args); err != nil {
		return options{}, err //nolint:wrapcheck // flag.Parse returns a flag.Error which is self-describing
	}
	opts.repoRoot = strings.TrimSpace(opts.repoRoot)
	opts.specPath = strings.TrimSpace(opts.specPath)
	opts.outPath = strings.TrimSpace(opts.outPath)
	if opts.repoRoot == "" {
		return options{}, fmt.Errorf("-repo-root is required")
	}
	if opts.specPath == "" {
		opts.specPath = filepath.Join(opts.repoRoot, "specs", "collector_fact_contract.v1.yaml")
	}
	if opts.outPath == "" {
		opts.outPath = filepath.Join(opts.repoRoot, "go", "internal", "collector", "contracttest", "contract_data.go")
	}
	return opts, nil
}

func loadSpec(path string) (*contractSpec, error) {
	data, err := os.ReadFile(path) // #nosec G304 -- reads operator-supplied spec file path
	if err != nil {
		return nil, fmt.Errorf("read contract spec: %w", err)
	}
	var spec contractSpec
	if err := yaml.Unmarshal(data, &spec); err != nil {
		return nil, fmt.Errorf("decode contract spec: %w", err)
	}
	if spec.Version == "" {
		return nil, fmt.Errorf("contract spec missing version field")
	}
	if len(spec.Collectors) == 0 {
		return nil, fmt.Errorf("contract spec must declare at least one collector")
	}
	for name, col := range spec.Collectors {
		if col.CollectorKind == "" {
			return nil, fmt.Errorf("collector %q missing collector_kind", name)
		}
		if len(col.FactKinds) == 0 {
			return nil, fmt.Errorf("collector %q must declare at least one fact kind", name)
		}
	}
	return &spec, nil
}

func generateGo(spec *contractSpec) ([]byte, error) {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by go/internal/collector/contracttest/gen; DO NOT EDIT.\n")
	buf.WriteString("// Source-of-truth: specs/collector_fact_contract.v1.yaml\n\n")
	buf.WriteString("package contracttest\n\n")

	collectorNames := sortedKeys(spec.Collectors)
	for _, name := range collectorNames {
		col := spec.Collectors[name]
		varName := toContractVarName(name)
		fmt.Fprintf(&buf, "// %s is the fact-shape contract for the %q collector.\n", varName, name)
		fmt.Fprintf(&buf, "var %s = Contract{\n", varName)
		fmt.Fprintf(&buf, "\tCollectorKind: %q,\n", col.CollectorKind)
		buf.WriteString("\tFactKinds: []FactKindShape{\n")
		factKindNames := sortedKeys(col.FactKinds)
		for _, fk := range factKindNames {
			keys := col.FactKinds[fk].RequiredPayloadKeys
			fmt.Fprintf(&buf, "\t\t{Kind: %q, RequiredPayloadKeys: []string{%s}},\n",
				fk, formatStringSlice(keys))
		}
		buf.WriteString("\t},\n")
		buf.WriteString("}\n\n")
	}

	buf.WriteString("// Contracts returns all collector contracts defined in the source-of-truth spec.\n")
	buf.WriteString("func Contracts() []Contract {\n")
	buf.WriteString("\treturn []Contract{\n")
	for _, name := range collectorNames {
		fmt.Fprintf(&buf, "\t\t%s,\n", toContractVarName(name))
	}
	buf.WriteString("\t}\n")
	buf.WriteString("}\n")

	return buf.Bytes(), nil
}

func toContractVarName(collectorKey string) string {
	// Map collector YAML key to exported Go variable name.
	switch collectorKey {
	case "aws":
		return "AWSContract"
	default:
		// PascalCase the collector key
		return strings.ToUpper(collectorKey[:1]) + collectorKey[1:] + "Contract"
	}
}

func formatStringSlice(keys []string) string {
	var parts []string
	for _, k := range keys {
		parts = append(parts, fmt.Sprintf("%q", k))
	}
	return strings.Join(parts, ", ")
}

func sortedKeys[V any](m map[string]V) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

func verifyFile(path string, want []byte) error {
	got, err := os.ReadFile(path) // #nosec G304 -- reads internally-constructed generated file path
	if err != nil {
		return fmt.Errorf("read generated file %s: %w", path, err)
	}
	if !bytes.Equal(got, want) {
		return fmt.Errorf("generated file %s is stale; run scripts/generate-contracttest.sh", path)
	}
	return nil
}

func writeFile(path string, contents []byte) error {
	if err := os.MkdirAll(filepath.Dir(path), 0o750); err != nil { // #nosec G301 -- internal generated-file output directory
		return fmt.Errorf("create generated file directory %s: %w", filepath.Dir(path), err)
	}
	current, err := os.ReadFile(path) // #nosec G304 -- reads internally-constructed generated file path
	if err == nil && bytes.Equal(current, contents) {
		return nil
	}
	if err := os.WriteFile(path, contents, 0o644); err != nil { // #nosec G306 -- generated Go source file
		return fmt.Errorf("write generated file %s: %w", path, err)
	}
	return nil
}
