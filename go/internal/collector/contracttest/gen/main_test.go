func TestRun_GenerateAndVerify(t *testing.T) {
	tmp := t.TempDir()
	specPath := filepath.Join(tmp, "collector_fact_contract.v1.yaml")
	outPath := filepath.Join(tmp, "contract_data.go")

	spec := []byte(`version: "1.0.0"
collectors:
  aws:
    collector_kind: aws
    fact_kinds:
      aws_resource:
        required_payload_keys:
          - account_id
          - region
          - service_kind
          - collector_instance_id
          - arn
          - resource_id
          - resource_type
          - name
          - state
          - tags
          - attributes
          - correlation_anchors
      aws_relationship:
        required_payload_keys:
          - account_id
          - region
          - service_kind
          - collector_instance_id
          - relationship_type
          - source_resource_id
          - source_arn
          - target_resource_id
          - target_arn
          - target_type
          - attributes
`)
	if err := os.WriteFile(specPath, spec, 0o644); err != nil {
		t.Fatal(err)
	}

	// Generate
	var genOut bytes.Buffer
	if err := run([]string{
		"-repo-root", tmp,
		"-spec", specPath,
		"-out", outPath,
	}, &genOut, &genOut); err != nil {
		t.Fatalf("generate: %v", err)
	}

	// Verify generated output exists and contains expected content
	data, err := os.ReadFile(outPath)
	if err != nil {
		t.Fatalf("read generated file: %v", err)
	}
	content := string(data)
	for _, want := range []string{
		"// Code generated by",
		"package contracttest",
		"var AWSContract = Contract{",
		`CollectorKind: "aws"`,
		`Kind: "aws_resource"`,
		`Kind: "aws_relationship"`,
		"func Contracts()",
		"AWSContract,",
	} {
		if !strings.Contains(content, want) {
			t.Errorf("generated output missing %q", want)
		}
	}

	// Verify -check passes on generated output
	var checkOut bytes.Buffer
	if err := run([]string{
		"-repo-root", tmp,
		"-spec", specPath,
		"-out", outPath,
		"-check",
	}, &checkOut, &checkOut); err != nil {
		t.Fatalf("check: %v", err)
	}

	// Corrupt output and verify -check fails
	if err := os.WriteFile(outPath, []byte("corrupted"), 0o644); err != nil {
		t.Fatal(err)
	}
	var staleOut bytes.Buffer
	if err := run([]string{
		"-repo-root", tmp,
		"-spec", specPath,
		"-out", outPath,
		"-check",
	}, &staleOut, &staleOut); err == nil {
		t.Fatal("expected -check to fail on corrupted output")
	} else if !strings.Contains(err.Error(), "stale") {
		t.Errorf("expected stale error, got: %v", err)
	}
}

