func TestRun_InvalidSpec_MissingVersion(t *testing.T) {
	tmp := t.TempDir()
	specPath := filepath.Join(tmp, "spec.yaml")
	if err := os.WriteFile(specPath, []byte("collectors:\n  aws:\n    collector_kind: aws\n    fact_kinds:\n      f: {required_payload_keys: [a]}\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	var buf bytes.Buffer
	err := run([]string{
		"-repo-root", tmp,
		"-spec", specPath,
		"-out", filepath.Join(tmp, "out.go"),
	}, &buf, &buf)
	if err == nil {
		t.Fatal("expected error for missing version")
	}
	if !strings.Contains(err.Error(), "version") {
		t.Errorf("expected version error, got: %v", err)
	}
}

