func TestToContractVarName(t *testing.T) {
	tests := []struct {
		input string
		want  string
	}{
		{"aws", "AWSContract"},
		{"azure", "AzureContract"},
		{"gcp", "GcpContract"},
	}
	for _, tt := range tests {
		got := toContractVarName(tt.input)
		if got != tt.want {
			t.Errorf("toContractVarName(%q) = %q, want %q", tt.input, got, tt.want)
		}
	}
}

