package gcpcloud

import (
	"fmt"
	"strings"
)

func stringify(v any) string {
	return fmt.Sprintf("%v", v)
}

func containsAny(haystack string, needles ...string) bool {
	for _, needle := range needles {
		if strings.Contains(haystack, needle) {
			return true
		}
	}
	return false
}
